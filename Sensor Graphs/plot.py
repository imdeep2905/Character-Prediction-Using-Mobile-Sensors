plt.legend()
plt.savefig("linear1.eps",format='eps')
plt.show()
plt.plot(data[:,6],label="X-axis")
plt.plot(data[:,7],label="Y-axis")
plt.plot(data[:,8],label="Z-axis")
plt.xlabel("Timestamp")
plt.ylabel("Value(rad/s)")
plt.title("Gyroscope")
plt.legend(loc = "lower right")
plt.savefig("gyro1.eps",format='eps')
plt.show()
plt.plot(data[:,9],label="X-axis")
plt.plot(data[:,10],label="Y-axis")
plt.plot(data[:,11],label="Z-axis")
plt.xlabel("Timestamp")
plt.ylabel("Value(μT)")
plt.title("Magnetic Field")
plt.legend(bbox_to_anchor=(0.77, 0.4), loc='upper left', borderaxespad=0.)
plt.savefig("mag1.eps",format='eps')
plt.show()

